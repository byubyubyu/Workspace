// 保存先: Assets/Scripts/Item/ItemState.cs
// 瓶の中のアイテム（BottleItemCore）の状態。
//   状態は2つの用途に使う：
//     (1) こぼれ／取り出しの分岐（Bottleが口の外で検知したとき、状態で使用かこぼれか無視かを決める）
//     (2) 将来の見た目演出（ドラッグ中を光らせる等）
//   初期実装で実際に使うのは Falling / Stored。Dragging / Spilling は将来の演出・分岐で使う（先に定義しておく）。
public enum ItemState
{
    Falling,  // 落下中（瓶に入れている途中。まだ収納前。こぼれ判定の対象外＝口の外に出ても無視）
    Stored,   // 収納済み（瓶の中で安定）。掴める・こぼれ判定の対象
    Dragging, // ドラッグ中（プレイヤーが漁って引っ張っている）。口から出たら取り出し成功＝使用
    Spilling, // こぼれ落下中（瓶から出てマップへ向かう途中）。将来の演出用
}
